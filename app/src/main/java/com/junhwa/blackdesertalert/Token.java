package com.junhwa.blackdesertalert;

public class Token {
    private String token;
    private String mac;

    public Token() {
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }

    public String getToken() {
        return token;
    }
}
